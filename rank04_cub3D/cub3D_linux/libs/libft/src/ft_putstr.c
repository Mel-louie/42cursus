#include "libft.h"

int		ft_putstr(char const *s)
{
	int i;

	i = 0;
	if (!s)
		return (-1);
	else
	{
		while (s[i])
		{
			ft_putchar_fd(s[i], 1);
			i++;
		}
	}
	return (i);
}
