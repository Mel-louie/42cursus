#include "libft.h"

char		*ft_strdup(const char *s)
{
	char	*dest;
	size_t	i;
	size_t	len;

	if (!s)
		return (NULL);
	len = ft_strlen((char*)s);
	dest = (char*)malloc(sizeof(char) * (len + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
